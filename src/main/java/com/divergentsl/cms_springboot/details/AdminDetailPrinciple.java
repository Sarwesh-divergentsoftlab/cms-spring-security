package com.divergentsl.cms_springboot.details;

import java.util.Collection;
import java.util.Collections;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.divergentsl.cms_springboot.model.LoginAdmin;

public class AdminDetailPrinciple implements UserDetails{

	private LoginAdmin loginAdmin;
	
	public AdminDetailPrinciple(LoginAdmin loginAdmin) {
		super();
		this.loginAdmin = loginAdmin;
	}

	@Override
	public Collection<? extends GrantedAuthority> getAuthorities() {
		return Collections.singleton(new SimpleGrantedAuthority("USER"));
	}

	@Override
	public String getPassword() {
		// TODO Auto-generated method stub
		return loginAdmin.getPassword();
	}

	@Override
	public String getUsername() {
		// TODO Auto-generated method stub
		return loginAdmin.getUsername();
	}

	@Override
	public boolean isAccountNonExpired() {
		// TODO Auto-generated method stub
		return true;
	}

	@Override
	public boolean isAccountNonLocked() {
		// TODO Auto-generated method stub
		return true;
	}

	@Override
	public boolean isCredentialsNonExpired() {
		// TODO Auto-generated method stub
		return true;
	}

	@Override
	public boolean isEnabled() {
		// TODO Auto-generated method stub
		return true;
	}

}
